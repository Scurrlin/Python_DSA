# In a Binary Serach Tree, each number across THE ENTIRE TREE follows strict
# rules regarding their relation to the top number/node.

# Every number that is LESS than the top number/node will be to the LEFT of the
# top number/node

# Every number that is HIGHER than the top number/node will be to the RIGHT of
# the top number/node

# LESS => LEFT
# HIGHER => RIGHT

# When comparing LLs and BSTs, the following functions are more efficient when
# using the accompanying data structure:

# insert() => LL
# lookup() => BST
# remove() => BST

